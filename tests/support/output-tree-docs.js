function buildPathTree(entries) {
  const root = { children: new Map(), type: 'directory' };

  for (const entry of entries) {
    const segments = entry.path.split('/');
    let node = root;

    for (let index = 0; index < segments.length; index += 1) {
      const name = segments[index];
      const isLeaf = index === segments.length - 1;

      if (!node.children.has(name)) {
        node.children.set(name, {
          children: new Map(),
          type: isLeaf ? entry.type : 'directory',
          mode: isLeaf ? entry.mode : null,
          target: isLeaf ? entry.target : null,
        });
      }
      node = node.children.get(name);
    }
  }

  return root;
}

function compareText(left, right) {
  if (left < right) return -1;
  if (left > right) return 1;
  return 0;
}

function renderPathTree(entries, rootLabel) {
  const root = buildPathTree(entries);
  const lines = [`${rootLabel}/`];

  function renderChildren(node, prefix) {
    const children = [...node.children.entries()]
      .sort(([left], [right]) => compareText(left, right));

    children.forEach(([name, child], index) => {
      const last = index === children.length - 1;
      const connector = last ? '└── ' : '├── ';
      const suffix = child.type === 'directory'
        ? '/'
        : child.type === 'symlink'
          ? ` -> ${child.target}`
          : child.mode === '0755'
            ? ' *'
            : '';
      lines.push(`${prefix}${connector}${name}${suffix}`);

      if (child.type === 'directory') {
        renderChildren(child, `${prefix}${last ? '    ' : '│   '}`);
      }
    });
  }

  renderChildren(root, '');
  return lines.join('\n');
}

function enabledNames(record) {
  return Object.entries(record)
    .filter(([, enabled]) => enabled)
    .map(([name]) => name)
    .join(', ');
}

export function renderOutputTreeContract(scenarios, manifests) {
  const manifestByScenario = new Map(manifests.map((manifest) => [manifest.scenario, manifest]));
  const lines = [
    '# Generated Output-Tree Contract',
    '',
    '<!-- Generated by tests/update-output-tree-contracts.js. Do not edit by hand. -->',
    '',
    'These source-only trees are generated with the stable toolchain and `--skip-install`.',
    'Each contract records every file or symlink path, Unix mode, byte length, and SHA-256 digest.',
    'Installed dependencies, compiler output, and framework caches are forbidden.',
    'Deterministic provider-placeholder `.env.example` files are hashed by the contract; generated real `.env` files are forbidden.',
    '',
    'Runtime E2E builds use disposable operating-system temp directories and are not part of these golden trees.',
    'A generated project may contain only the root `pnpm-workspace.yaml` and root `pnpm-lock.yaml`; nested pnpm workspace islands are rejected.',
    '',
    'Regenerate after an intentional scaffold change:',
    '',
    '```sh',
    'pnpm build',
    'node tests/update-output-tree-contracts.js',
    '```',
    '',
    '## Scenario summary',
    '',
    '| Scenario | Apps | Features | Contract | Entries | Tree SHA-256 |',
    '| --- | --- | --- | --- | ---: | --- |',
  ];

  for (const scenario of scenarios) {
    const manifest = manifestByScenario.get(scenario.id);
    if (scenario.goldenTree && !manifest) throw new Error(`Missing output-tree manifest for ${scenario.id}`);
    lines.push(manifest
      ? `| \`${scenario.id}\` | ${enabledNames(scenario.apps)} | ${enabledNames(scenario.features)} | deterministic golden | ${manifest.entries.length} | \`${manifest.digest}\` |`
      : `| \`${scenario.id}\` | ${enabledNames(scenario.apps)} | ${enabledNames(scenario.features)} | installed public E2E | — | — |`);
  }

  for (const scenario of scenarios) {
    const manifest = manifestByScenario.get(scenario.id);
    if (!manifest) continue;
    lines.push(
      '',
      `## ${scenario.id}`,
      '',
      scenario.description,
      '',
      '```text',
      renderPathTree(manifest.entries, scenario.projectDirectory),
      '```',
    );
  }

  lines.push('');
  return lines.join('\n');
}
