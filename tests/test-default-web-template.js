import assert from 'node:assert/strict';
import { existsSync, mkdtempSync, readFileSync, rmSync } from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const tempRoot = mkdtempSync(path.join(os.tmpdir(), 'anhedral-default-web-template-'));

try {
  const { writeDefaultWebEnvExample, writeDefaultWebFiles } = await import(path.join(repoRoot, 'dist', 'templates', 'default-web.js'));

  writeDefaultWebEnvExample(tempRoot);
  writeDefaultWebFiles(tempRoot, 'Demo App');

  const nextConfigPath = path.join(tempRoot, 'next.config.mjs');
  const clerkHelperPath = path.join(tempRoot, 'lib', 'auth', 'clerk.ts');
  const buttonHelperPath = path.join(tempRoot, 'lib', 'ui', 'button.ts');
  const buttonComponentPath = path.join(tempRoot, 'components', 'ui', 'button.tsx');
  const signInPanelPath = path.join(tempRoot, 'components', 'auth', 'sign-in-panel.tsx');
  const signUpPanelPath = path.join(tempRoot, 'components', 'auth', 'sign-up-panel.tsx');
  const themeTogglePath = path.join(tempRoot, 'components', 'theme-toggle.tsx');
  const homePagePath = path.join(tempRoot, 'app', 'page.tsx');
  const dashboardPagePath = path.join(tempRoot, 'app', 'dashboard', 'page.tsx');
  const layoutPath = path.join(tempRoot, 'app', 'layout.tsx');
  const signInPagePath = path.join(tempRoot, 'app', 'sign-in', '[[...sign-in]]', 'page.tsx');
  const signUpPagePath = path.join(tempRoot, 'app', 'sign-up', '[[...sign-up]]', 'page.tsx');
  const demoEnvPath = path.join(tempRoot, 'lib', 'demo-env.ts');
  const globalsPath = path.join(tempRoot, 'app', 'globals.css');
  const userMenuPath = path.join(tempRoot, 'components', 'dashboard', 'header-user-menu.tsx');
  const avatarRoutePath = path.join(tempRoot, 'app', 'api', 'account', 'avatar', 'route.ts');
  const checkoutRoutePath = path.join(tempRoot, 'app', 'api', 'stripe', 'checkout', 'route.ts');
  const stripeHelperPath = path.join(tempRoot, 'lib', 'payments', 'stripe.ts');
  const logoPath = path.join(tempRoot, 'public', 'anhedral.svg');
  const faviconPath = path.join(tempRoot, 'app', 'favicon.ico');
  const legacyLogoPath = path.join(tempRoot, 'public', 'anhedral-mark.svg');

  assert.equal(existsSync(nextConfigPath), true, 'next.config.mjs should exist');
  assert.equal(existsSync(clerkHelperPath), true, 'shared Clerk helper should exist');
  assert.equal(existsSync(buttonHelperPath), true, 'shared button helper should exist');
  assert.equal(existsSync(buttonComponentPath), true, 'button component should exist');
  assert.equal(existsSync(signInPanelPath), true, 'custom sign-in panel should exist');
  assert.equal(existsSync(signUpPanelPath), true, 'custom sign-up panel should exist');
  assert.equal(existsSync(themeTogglePath), true, 'theme toggle should exist');
  assert.equal(existsSync(homePagePath), true, 'home page should exist');
  assert.equal(existsSync(dashboardPagePath), true, 'dashboard page should exist');
  assert.equal(existsSync(layoutPath), true, 'layout should exist');
  assert.equal(existsSync(signInPagePath), true, 'sign-in page should exist');
  assert.equal(existsSync(signUpPagePath), true, 'sign-up page should exist');
  assert.equal(existsSync(globalsPath), true, 'globals.css should exist');
  assert.equal(existsSync(userMenuPath), true, 'header user menu should exist');
  assert.equal(existsSync(avatarRoutePath), true, 'avatar route should exist');
  assert.equal(existsSync(stripeHelperPath), true, 'stripe helper should exist');
  assert.equal(existsSync(logoPath), true, 'anhedral.svg logo should exist');
  assert.equal(existsSync(faviconPath), true, 'favicon.ico should exist');
  assert.equal(existsSync(legacyLogoPath), false, 'legacy anhedral-mark.svg should not exist');

  const nextConfig = readFileSync(nextConfigPath, 'utf8');
  const clerkHelper = readFileSync(clerkHelperPath, 'utf8');
  const buttonHelper = readFileSync(buttonHelperPath, 'utf8');
  const buttonComponent = readFileSync(buttonComponentPath, 'utf8');
  const signInPanel = readFileSync(signInPanelPath, 'utf8');
  const signUpPanel = readFileSync(signUpPanelPath, 'utf8');
  const themeToggle = readFileSync(themeTogglePath, 'utf8');
  const homePage = readFileSync(homePagePath, 'utf8');
  const dashboardPage = readFileSync(dashboardPagePath, 'utf8');
  const layout = readFileSync(layoutPath, 'utf8');
  const signInPage = readFileSync(signInPagePath, 'utf8');
  const signUpPage = readFileSync(signUpPagePath, 'utf8');
  const demoEnv = readFileSync(demoEnvPath, 'utf8');
  const globals = readFileSync(globalsPath, 'utf8');
  const userMenu = readFileSync(userMenuPath, 'utf8');
  const avatarRoute = readFileSync(avatarRoutePath, 'utf8');
  const checkoutRoute = readFileSync(checkoutRoutePath, 'utf8');
  const stripeHelper = readFileSync(stripeHelperPath, 'utf8');
  const sourceLogo = readFileSync(path.join(repoRoot, 'anhedral.svg'), 'utf8');
  const generatedLogo = readFileSync(logoPath, 'utf8');
  const sourceFavicon = readFileSync(path.join(repoRoot, 'favicon.ico'));
  const generatedFavicon = readFileSync(faviconPath);

  assert.match(nextConfig, /reactCompiler:\s*true/);
  assert.match(clerkHelper, /export function getClerkProfile/);
  assert.match(clerkHelper, /export function getPrimaryEmail/);
  assert.match(buttonHelper, /export const buttonVariants = cva/);
  assert.match(buttonComponent, /import \{ type ButtonVariantProps, buttonVariants \} from '@\/lib\/ui\/button';/);
  assert.match(signInPanel, /useSignIn/);
  assert.match(signUpPanel, /useSignUp/);
  assert.match(signInPanel, /signIn\.password/);
  assert.match(signInPanel, /signIn\.finalize/);
  assert.match(signUpPanel, /signUp\.password/);
  assert.match(signUpPanel, /signUp\.verifications\.sendEmailCode/);
  assert.match(signUpPanel, /signUp\.verifications\.verifyEmailCode/);
  assert.match(signUpPanel, /signUp\.finalize/);
  assert.doesNotMatch(signInPanel, /<SignIn/);
  assert.doesNotMatch(signUpPanel, /<SignUp/);
  assert.doesNotMatch(signInPanel, /setActive/);
  assert.doesNotMatch(signUpPanel, /setActive/);
  assert.doesNotMatch(signUpPanel, /prepareEmailAddressVerification/);
  assert.doesNotMatch(signUpPanel, /attemptEmailAddressVerification/);
  assert.match(themeToggle, /useTheme/);
  assert.match(themeToggle, /Switch to dark mode/);
  assert.match(themeToggle, /size="icon"/);
  assert.doesNotMatch(themeToggle, /Light mode/);
  assert.doesNotMatch(themeToggle, /Dark mode/);
  assert.match(homePage, /import \{ buttonVariants \} from '@\/lib\/ui\/button';/);
  assert.match(homePage, /import \{ ThemeToggle \} from '@\/components\/theme-toggle';/);
  assert.match(homePage, /requiredEnvGroups/);
  assert.match(homePage, /homePageContent/);
  assert.match(homePage, /linkLabel/);
  assert.match(homePage, /buttonVariants\(\{ size: 'sm', variant: 'link' \}\)/);
  assert.match(homePage, /break-all font-mono text-xs/);
  assert.doesNotMatch(homePage, /DemoEnvSetupCard/);
  assert.match(homePage, /<main className="min-h-screen bg-background">/);
  assert.match(homePage, /<header className="border-b">/);
  assert.match(homePage, /Anhedral/);
  assert.match(homePage, /<ThemeToggle \/>/);
  assert.doesNotMatch(homePage, /radial-gradient/);
  assert.doesNotMatch(homePage, /amber-/);
  assert.match(demoEnv, /export const clerkEnvReady =/);
  assert.match(demoEnv, /hasRealValue\(process\.env\.NEXT_PUBLIC_CLERK_PUBLISHABLE_KEY\)/);
  assert.match(demoEnv, /hasRealValue\(process\.env\.CLERK_SECRET_KEY\)/);
  assert.doesNotMatch(demoEnv, /isValidPublishableKey/);
  assert.doesNotMatch(demoEnv, /isValidSecretKey/);
  assert.match(dashboardPage, /import \{ getClerkProfile \} from '@\/lib\/auth\/clerk';/);
  assert.match(dashboardPage, /import \{ clerkEnvReady \} from '@\/lib\/demo-env';/);
  assert.match(dashboardPage, /import \{ buttonVariants \} from '@\/lib\/ui\/button';/);
  assert.match(dashboardPage, /import \{ ThemeToggle \} from '@\/components\/theme-toggle';/);
  assert.match(dashboardPage, /if \(!clerkEnvReady\) \{\s*redirect\('\/'\);/s);
  assert.match(dashboardPage, /<ThemeToggle \/>/);
  assert.doesNotMatch(dashboardPage, /amber-/);
  assert.doesNotMatch(dashboardPage, /user\?\.firstName/);
  assert.match(signInPage, /SignInPanel/);
  assert.match(signUpPage, /SignUpPanel/);
  assert.match(signInPage, /import \{ redirect \} from 'next\/navigation';/);
  assert.match(signInPage, /import \{ clerkEnvReady \} from '@\/lib\/demo-env';/);
  assert.match(signInPage, /if \(!clerkEnvReady\) \{\s*redirect\('\/'\);/s);
  assert.match(signUpPage, /import \{ redirect \} from 'next\/navigation';/);
  assert.match(signUpPage, /import \{ clerkEnvReady \} from '@\/lib\/demo-env';/);
  assert.match(signUpPage, /if \(!clerkEnvReady\) \{\s*redirect\('\/'\);/s);
  assert.doesNotMatch(signInPage, /import \{ SignIn \} from '@clerk\/nextjs';/);
  assert.doesNotMatch(signUpPage, /import \{ SignUp \} from '@clerk\/nextjs';/);
  assert.doesNotMatch(layout, /@clerk\/ui\/themes/);
  assert.match(layout, /title: 'Anhedral'/);
  assert.match(layout, /icon: '\/favicon\.ico'/);
  assert.doesNotMatch(layout, /fixed top-4 right-4/);
  assert.match(userMenu, /import Image from 'next\/image';/);
  assert.match(userMenu, /useClerk/);
  assert.doesNotMatch(userMenu, /SignOutButton/);
  assert.doesNotMatch(userMenu, /useTransition/);
  assert.doesNotMatch(userMenu, /<img/);
  assert.doesNotMatch(globals, /@clerk\/ui\/themes\/shadcn\.css/);
  assert.match(avatarRoute, /import \{ getClerkProfile \} from '@\/lib\/auth\/clerk';/);
  assert.match(checkoutRoute, /stripe\.customers\.create/);
  assert.match(checkoutRoute, /customer: stripeCustomerId,/);
  assert.doesNotMatch(checkoutRoute, /session\.customer/);
  assert.match(stripeHelper, /return new Stripe\(process\.env\.STRIPE_SECRET_KEY\);/);
  assert.doesNotMatch(stripeHelper, /apiVersion:/);
  assert.equal(generatedLogo, sourceLogo);
  assert.deepEqual(generatedFavicon, sourceFavicon);

  console.log('Default web template regression test passed');
} finally {
  rmSync(tempRoot, { recursive: true, force: true });
}
